import { Schema, model } from 'mongoose'
import { IUserDocument } from '../../types/User'

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  updated: {
    type: Date,
    default: Date.now
  },
  created: {
    type: Date,
    default: Date.now
  }
})

export const User = model<IUserDocument>('user', UserSchema)
